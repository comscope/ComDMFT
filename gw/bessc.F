	subroutine bessc(x,bes,besd,lmax)
******************************************************************
*   caclculate bessel functions of a general (complex) argument  *
*   as well as their derivatives with respect to argument.       *
******************************************************************
	implicit none
	integer, intent(in) :: lmax
	complex*16, intent(inout) :: x
	complex*16, intent(out) :: bes(0:lmax),besd(0:lmax)
	integer :: l00,lup,li,il
	real*8 :: fac2
	complex*16 :: sqbn,ww,bnor
	complex*16, allocatable :: besw(:)
	allocate(besw(0:26))
      if (abs(x).gt.10.d0) then       ! direct recursion
	  bes(0)=sin(x)/x
	  besd(0)=-(sin(x)/x-cos(x))/x
	  if(lmax.gt.0) then
	    bes(1)=-besd(0)
	    besd(1)=bes(0)-2.d0*bes(1)/x
	  endif
	  do li=0,lmax-2
	    bes(li+2)=dble(2*li+3)/x*bes(li+1)-bes(li)
	    besd(li+2)=bes(li+1)-dble(li+3)*bes(li+2)/x
	  enddo
      else                    ! reverse recursion
	  do il=0,lmax
	    bes(il)=(0.d0,0.d0)
	    besd(il)=(0.d0,0.d0)
	  enddo
	  if(abs(x).lt.1.d-08) then
	    bes(0)=(1.d0,0.d0)
	    besd(0)=(0.d0,0.d0)
	    if(lmax.gt.0) besd(1)=dcmplx(1.d0/3.d0,0.d0)
	    return
	  endif
	  if(abs(x).eq.1.d0) x=1.d0+1.d-06
	  l00=-24.d0/log10(abs(x))
	  lup=min(26,l00)
	  if (l00.le.0) lup=26
1       ww=x**lup/fac2(2*lup+1)*(1.d0-x**2/2.d0/dble(2*lup+3))
	  if(abs(ww).eq.0.d0) then
	    lup=lup-1
	    goto 1
	  endif
	  besw(lup)=ww
	  besw(lup-1)=x**(lup-1)/fac2(2*lup-1)*(1.d0-x**2/2.d0
     &	           /dble(2*lup+1))
	  bnor=dble(2*lup+1)*besw(lup)*besw(lup)+
     &       dble(2*lup-1)*besw(lup-1)*besw(lup-1)
	  do li=lup-2,0,-1
	    besw(li)=dble(2*li+3)/x*besw(li+1)-besw(li+2)
	    bnor=bnor+dble(2*li+1)*besw(li)*besw(li)
	  enddo
	  sqbn=sqrt(bnor)
	  bes(0)=besw(0)/sqbn
	  besd(0)=(cos(x)-sin(x)/x)/x
	  do li=1,min(lmax,lup)
	    bes(li)=besw(li)/sqbn
	    besd(li)=bes(li-1)-dble(li+1)*bes(li)/x
	  enddo
      endif
	deallocate(besw)
	end
	
	subroutine bessel(r,e,s,bes,lmax)
************************************************************
*  calculate bessel functions j defined as follows:        *
*                     (2l+1)!!                             *
*            j (kr)=  -------- j (kr)                      *
*             l        (ks)^l   l                          *
*  and their derivative with respect to r. here k=sqrt(e)  *
*  and j(x) is a spherical bessel function.                *
************************************************************
	implicit none
	integer, intent(in) :: lmax
	real*8, intent(in) :: r,e,s
	real*8, intent(out) :: bes(3,0:lmax)
	integer :: l
	real*8 :: fl,fac2,k
	complex*16 :: x,t
	complex*16, allocatable :: best(:),bestd(:)
	allocate(best(0:lmax))
	allocate(bestd(0:lmax))
	k=sqrt(-e)
	x=r*k*(0.d0,1.d0)     
	t=s*k*(0.d0,1.d0)
	call bessc(x,best,bestd,lmax)
	if(r.eq.0.d0)then
	  do l=0,lmax
	    bes(1,l)=0.d0
	    bes(2,l)=0.d0
	    bes(3,l)=0.d0
	  enddo
	  bes(1,0)=1.d0
	  if(lmax.gt.0) bes(2,1)=1.d0
	  if(lmax.gt.1) bes(3,2)=2.d0
	  return
	endif
	do l=0,lmax
	  fl=fac2(2*l+1)
	  bes(1,l)=fl*best(l)/(t**l)
	  bes(2,l)=fl*bestd(l)/(t**(l-1))/s
	  bes(3,l)=((dble(l*(l+1))/r/r-e)*bes(1,l)-2.d0/r*bes(2,l))
	enddo
	deallocate(best,bestd)
	end
	
	SUBROUTINE BESSR(k,r,BES,BESD,LMAX)
******************************************************************
*   Caclculate Bessel functions of a real argument               *
*   as well as their derivatives with respect to argument.       *
******************************************************************
      IMPLICIT none
	integer :: lmax,li,il,l,l00,lup
      real*8 :: k,x,BESW(0:26),BES(0:LMAX),BESD(0:LMAX),ww,bnor,sqbn,r,
     &	        fac2,y
C
c       IF(LMAX.GT.26)STOP'<BESSC> LMAX>26 DOES NOT WORK'
c       IF(LMAX.GT.20)PRINT*,'LMAX EXCEEDED 20 IN BESSR FUNCTION'
C
      x=k*r
      IF (ABS(X).GT.10.D0) THEN       ! DIRECT RECURSION
        BES(0)=SIN(X)/X
        BESD(0)=-(SIN(X)/X-COS(X))/X
        IF(LMAX.GT.0)BES(1)=-BESD(0)
        IF(LMAX.GT.0)BESD(1)=BES(0)-2.D0*BES(1)/X
        DO LI=0,LMAX-2
          BES(LI+2)=DBLE(2*LI+3)/X*BES(LI+1)-BES(LI)
          BESD(LI+2)=BES(LI+1)-DBLE(LI+3)*BES(LI+2)/X
	  enddo
      ELSE                    ! REVERSE RECURSION
        DO IL=0,LMAX
          BES(IL)=0.D0
          BESD(IL)=0.D0
	  enddo
        IF(ABS(X).LT.1.D-08) THEN
          BES(0)=1.D0
          BESD(0)=0.D0
          do l=1,lmax
            if(l.eq.1) then
               besd(l)=k/3.d0
            else
               besd(l)=l*k**l*r**(l-1)/fac2(l+l+1)
            endif
            bes(l)=r*besd(l)/l
          enddo
          RETURN
        ENDIF
        IF(ABS(X).EQ.1.D0) X=1.D0+1.D-06
        y=-24.0d0/log10(abs(x))
c
c       Y may actually exceed MAXINT causing arithmetic exceptions
c       in int(y), or cause int(y) to produce negative numbers. To
c       avoid such problems intercept this special case.
c
        if (y.gt.26.0d0.or.y.lt.0.0d0) then
          L00=26
        else
          L00=int(y)
        endif
        LUP=MIN(26,L00)
        IF (L00.LE.0) LUP=26
c
1       WW=X**LUP/FAC2(2*LUP+1)*(1.D0-X**2/2.D0/DBLE(2*LUP+3))
        IF(ABS(WW).EQ.0.D0) THEN
          LUP=LUP-1
          GOTO 1
        ENDIF
        BESW(LUP)=WW
        BESW(LUP-1)=X**(LUP-1)/FAC2(2*LUP-1)*(1.D0-
     &             X**2/2.D0/DBLE(2*LUP+1))
        BNOR=DBLE(2*LUP+1)*BESW(LUP)*BESW(LUP)+
     &  DBLE(2*LUP-1)*BESW(LUP-1)*BESW(LUP-1)
        DO LI=LUP-2,0,-1
          BESW(LI)=DBLE(2*LI+3)/X*BESW(LI+1)-BESW(LI+2)
          BNOR=BNOR+DBLE(2*LI+1)*BESW(LI)*BESW(LI)
	  enddo
        SQBN=SQRT(BNOR)
        BES(0)=BESW(0)/SQBN
        BESD(0)=(COS(X)-SIN(X)/X)/X
        DO LI=1,MIN(LMAX,LUP)
          BES(LI)=BESW(LI)/SQBN
          BESD(LI)=BES(LI-1)-DBLE(LI+1)*BES(LI)/X
	  enddo
      ENDIF
      do l=0,lmax
        besd(l)=k*besd(l)
      enddo
      END
      
      
      
